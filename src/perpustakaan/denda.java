    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (btnEdit.getText() == "btnEdit"){
            txtNama.requestFocus();
        }else{
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            String Nama = txtNama.getText();
            String Nama_Buku = txtBuku.getText();
            String Tanggal_Pinjam = String.valueOf(format.format(dtpTglPinjam.getDate()));
            String Tanggal_Kembali = String.valueOf(format.format(dtpTglKembali.getDate()));
            String Denda = txtDenda.getText();
            try{
                Statement statement = (Statement)koneksi.getConnection().createStatement();
                statement.executeUpdate("update tb_denda set Nama_Anggota='"+ Nama +"',Nama_Buku='"+ Nama_Buku +"',Tanggal_Pinjam='"+ Tanggal_Pinjam +"',Tanggal_Kembali='"+ Tanggal_Kembali +"',Jumlah_Keterlambatan = datediff(Tanggal_Kembali, Tanggal_Pinjam)-7,Total_Denda='"+ Denda +"'*Jumlah_Keterlambatan WHERE Nama_Anggota='"+ Nama +"' OR Nama_Buku='"+ Nama_Buku +"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                txtNama.setText("");
                txtBuku.setText("");
                dtpTglPinjam.setDateFormatString(null);
                dtpTglKembali.setDateFormatString(null);
            }catch(SQLException t){
                JOptionPane.showMessageDialog(null, t);
            }
            datatable();
        }
    }//GEN-LAST:event_btnEditActionPerformed

