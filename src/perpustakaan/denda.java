    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String Nama = txtNama.getText();
        String Buku = txtBuku.getText();
        String Tanggal_Pinjam = String.valueOf(format.format(dtpTglPinjam.getDate()));
        String Tanggal_Kembali = String.valueOf(format.format(dtpTglKembali.getDate()));
        String Denda = txtDenda.getText();
        try{
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            statement.executeUpdate("insert into tb_denda VALUES ('"+ Nama +"','"+ Buku +"','"+ Tanggal_Pinjam +"','"+ Tanggal_Kembali +"',datediff(Tanggal_Kembali, Tanggal_Pinjam)-7,'"+ Denda +"'*Jumlah_Keterlambatan);");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            txtNama.setText("");
            txtBuku.setText("");
            dtpTglPinjam.setDateFormatString(null);
            dtpTglKembali.setDateFormatString(null);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        datatable();
    }//GEN-LAST:event_jButtonSimpanActionPerformed

