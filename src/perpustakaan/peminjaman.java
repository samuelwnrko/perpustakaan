    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try{
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from tb_peminjaman where "+"NIM like '%"+txtSearch.getText()+"%' OR "+"Nama like '%"+txtSearch.getText()+"%'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("NIM");
            tbl.addColumn("Nama");
            tbl.addColumn("Kode_Buku");
            tbl.addColumn("Nama_Buku");
            tbl.addColumn("Tanggal_Pinjam");
            tbl.addColumn("Tanggal_Kembali");
            
            tb_peminjaman.setModel(tbl);
            while(res.next()){
                tbl.addRow(new Object[]{
                    res.getString("NIM"),
                    res.getString("Nama"),
                    res.getString("Kode_Buku"),
                    res.getString("Nama_Buku"),
                    res.getString("Tanggal_Pinjam"),
                    res.getString("Tanggal_Kembali"),
                });
            tb_peminjaman.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
                
        txtSearch.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

