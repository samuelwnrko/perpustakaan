    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        String NIM = txtNim.getText();
        String Nama = txtNama.getText();
        String Kode_Buku = id_buku.getText();
        String Nama_Buku = txtBuku.getText();
        String Tanggal_Pinjam = txtPinjam.getText();
        String Tanggal_Kembali = txtKembali.getText();
        String Status = "Dipinjam";
        try{
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            statement.executeUpdate("insert into tb_peminjaman VALUES ('"+ NIM +"','"+ Nama +"','"+ Kode_Buku +"','"+ Nama_Buku +"','"+ Tanggal_Pinjam +"','"+ Tanggal_Kembali +"');");
            statement.executeUpdate("update tb_buku SET Status ='"+ Status +"' WHERE Kode_Buku ='"+ Kode_Buku +"';");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            txtNim.setText("");
            txtNama.setText("");
            id_buku.setText("");
            txtBuku.setText("");
            txtPinjam.setText("");
            txtKembali.setText("");
        }catch(Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
        datatable();
        datatable1();
        tanggal();
    }//GEN-LAST:event_btnSaveActionPerformed

